+ (void)dumpMeshTransform:(BCMeshTransform *)meshTransform
{
    for (NSUInteger i = 0; i < meshTransform.vertexCount; i++) {
        BCMeshVertex vertex = [meshTransform vertexAtIndex:i];
        myLog(@"{%f, %f, %f, %f, %f}", vertex.from.x, vertex.from.y, vertex.to.x, vertex.to.y, vertex.to.z);
        if (i < meshTransform.vertexCount - 1) {
            myLog(@",");
        }
        myLog(@"\n");
    }
    
    for (NSUInteger i = 0; i < meshTransform.faceCount; i++) {
        BCMeshFace face = [meshTransform faceAtIndex:i];
        myLog(@"{%u, %u, %u, %u}", face.indices[0], face.indices[1], face.indices[2], face.indices[3]);
        if (i < meshTransform.faceCount - 1) {
            myLog(@",");
        }
        myLog(@"\n");
    }
}

